// Helper function to get translated text using the dictionary
export const getTranslatedText = (englishText, language) => {
  if (language === 'en') {
    return englishText;
  }

  // Search through all categories for the translation
  for (const category of Object.values(translations)) {
    if (category[englishText]) {
      return category[englishText];
    }
  }

  // If no translation found, return original text
  return englishText;
};

// Get translation by category and key (more efficient for known categories)
export const getTranslation = (category, key, language) => {
  if (language === 'en') {
    return key;
  }

  const categoryTranslations = translations[category];
  return categoryTranslations?.[key] || key;
};
